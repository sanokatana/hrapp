<script>
    $(function() {
        $('#btnTambahShift').click(function() {
            $('#modal-inputshift').modal("show");
        });

        $('.edit').click(function() {
            var id = $(this).attr('id');
            $.ajax({
                type: 'POST',
                url: '/shift/edit',
                cache: false,
                data: {
                    _token: "{{ csrf_token();}}",
                    id: id
                },
                success: function(respond) {
                    $('#loadeditform').html(respond);
                }
            });
            $('#modal-editshift').modal("show");
        });

        $(".delete-confirm").click(function(e) {
            var form = $(this).closest('form');
            e.preventDefault();
            Swal.fire({
                title: "Apakah Yakin?",
                text: "Data Cabang Akan Ke Delete!",
                icon: "warning",
                showCancelButton: true,
                confirmButtonColor: "#3085d6",
                cancelButtonColor: "#d33",
                confirmButtonText: "Delete"
            }).then((result) => {
                if (result.isConfirmed) {
                    form.submit();
                }
            });
        });

        $('#formShift').submit(function(event) {
            function showWarning(message, selector) {
                Swal.fire({
                    title: 'Warning!',
                    text: message,
                    icon: 'warning',
                    confirmButtonText: 'Ok'
                }).then(() => {
                    $(selector).focus();
                });
            }

            var fields = [{
                    selector: '#shift_name',
                    message: 'Shift Name Harus Diisi'
                },
                {
                    selector: '#type',
                    message: 'Type Harus Diisi'
                },
                {
                    selector: '#description',
                    message: 'Description Harus Diisi'
                },
                {
                    selector: '#status',
                    message: 'Status Harus Diisi'
                }
            ];

            for (var i = 0; i < fields.length; i++) {
                if ($(fields[i].selector).val() === "") {
                    showWarning(fields[i].message, fields[i].selector);
                    return false;
                }
            }

            // No need to call event.preventDefault() or this.submit() here
            // because if the form is valid, it will submit naturally
        });
    });
</script>
